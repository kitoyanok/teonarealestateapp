def parse_markdown_blocks(text: str):
    lines = text.splitlines()
    i = 0
    skip_headings = (
        "Текст под",
        "Текстовое описание сущностей",
        "Завершающий текст",
        "Материалы приложения",
        "Текст для раздела",
    )
    while i < len(lines):
        raw = lines[i].rstrip()
        stripped = raw.strip()
        if not stripped or stripped == "---":
            i += 1
            continue
        if stripped.startswith("|"):
            block = []
            while i < len(lines) and lines[i].strip().startswith("|"):
                block.append(lines[i].strip())
                i += 1
            rows = []
            for idx, row in enumerate(block):
                parts = [clean_inline(part) for part in row.strip("|").split("|")]
                if idx == 1 and set("".join(parts)) <= {"-", " "}:
                    continue
                rows.append(parts)
            yield ("table", rows)
            continue
        if stripped.startswith("#"):
            level = len(stripped) - len(stripped.lstrip("#"))
            heading_text = clean_inline(stripped[level:].strip())
            if heading_text.startswith(skip_headings):
                i += 1
                continue
            if heading_text.startswith("Рисунок"):
                yield ("figure", heading_text)
            elif heading_text.startswith("Таблица"):
                yield ("table_caption", heading_text)
            else:
                yield ("heading", level, heading_text)
            i += 1
            continue
        if stripped.startswith("`") and stripped.endswith("`"):
            caption = clean_inline(stripped.strip("`"))
            if caption.startswith("Рисунок"):
                yield ("figure", caption)
                i += 1
                continue
        if stripped.startswith("- "):
            yield ("bullet", stripped[2:])
            i += 1
            continue
        if re.match(r"^\d+\.", stripped):
            yield ("numbered", stripped)
            i += 1
            continue
        paragraph_lines = [stripped]
        i += 1
        while i < len(lines):
            nxt = lines[i].strip()
            if (
                not nxt
                or nxt == "---"
                or nxt.startswith("#")
                or nxt.startswith("|")
                or nxt.startswith("- ")
                or re.match(r"^\d+\.", nxt)
                or (nxt.startswith("`") and nxt.endswith("`"))
            ):
                break
            paragraph_lines.append(nxt)
            i += 1
        yield ("paragraph", " ".join(paragraph_lines))


def append_main_sections(builder: DocBuilder):
    for block in parse_markdown_blocks(build_main_source()):
        kind = block[0]
        if kind == "heading":
            _, level, text = block
            if level == 1:
                builder.paragraph(text, "h1")
            elif level == 2:
                builder.paragraph(text, "h2")
            else:
                builder.paragraph(text, "h3")
        elif kind == "paragraph":
            builder.paragraph(block[1], "body")
        elif kind == "bullet":
            builder.bullet(block[1])
        elif kind == "numbered":
            builder.numbered(block[1])
        elif kind == "figure":
            builder.figure(block[1])
        elif kind == "table_caption":
            builder.table_caption(block[1])
        elif kind == "table":
            rows = block[1]
            template_name = "four_col" if len(rows[0]) >= 4 else "two_col"
            builder.table(rows, template_name)


def appendix_heading(builder: DocBuilder, label: str, title: str):
    builder.page_break()
    builder.paragraph(label, "appendix")
    builder.paragraph(title, "appendix_title")


def add_two_col_field_table(builder: DocBuilder, caption: str, fields: list[tuple[str, str]]):
    builder.table_caption(caption)
    rows = [["Поле", "Содержание"]] + [[name, value] for name, value in fields]
    builder.table(rows, "two_col")


