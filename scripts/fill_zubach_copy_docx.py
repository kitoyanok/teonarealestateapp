def clean_inline(text: str) -> str:
    text = text.replace("`", "")
    text = re.sub(r"\*\*(.*?)\*\*", r"\1", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text


def copy_run_format(run, template_run) -> None:
    if template_run is None:
        return
    run.bold = template_run.bold
    run.italic = template_run.italic
    run.underline = template_run.underline
    run.font.name = template_run.font.name
    if template_run.font.size:
        run.font.size = template_run.font.size
    if template_run.font.color and template_run.font.color.rgb:
        run.font.color.rgb = RGBColor(
            template_run.font.color.rgb[0],
            template_run.font.color.rgb[1],
            template_run.font.color.rgb[2],
        )
    if run._element.rPr is not None and template_run._element.rPr is not None:
        fonts = template_run._element.rPr.rFonts
        if fonts is not None:
            rpr = run._element.get_or_add_rPr()
            rfonts = rpr.rFonts
            if rfonts is None:
                rfonts = OxmlElement("w:rFonts")
                rpr.append(rfonts)
            for key in ("ascii", "hAnsi", "cs", "eastAsia"):
                value = fonts.get(qn(f"w:{key}"))
                if value:
                    rfonts.set(qn(f"w:{key}"), value)


def first_template_run(paragraph):
    for run in paragraph.runs:
        return run
    return None


def apply_paragraph_template(paragraph, template) -> None:
    paragraph.style = template.style
    paragraph.alignment = template.alignment
    dst = paragraph.paragraph_format
    src = template.paragraph_format
    for attr in (
        "left_indent",
        "right_indent",
        "first_line_indent",
        "keep_together",
        "keep_with_next",
        "page_break_before",
        "widow_control",
        "space_before",
        "space_after",
        "line_spacing",
        "line_spacing_rule",
    ):
        setattr(dst, attr, getattr(src, attr))


class DocBuilder:
    def __init__(self, doc: Document):
        self.doc = doc
        self.templates = {
            "h1": doc.paragraphs[11],
            "h2": doc.paragraphs[19],
            "h3": doc.paragraphs[188],
            "body": doc.paragraphs[14],
            "normal": doc.paragraphs[31],
            "figure": doc.paragraphs[28],
            "table_caption": doc.paragraphs[42],
            "appendix": doc.paragraphs[415],
            "appendix_title": doc.paragraphs[431],
        }
        self.table_templates = {
            "two_col": doc.tables[1],
            "four_col": doc.tables[8],
        }

    def paragraph(self, text: str, kind: str = "body", blank_before: bool = False):
        if blank_before:
            self.doc.add_paragraph("")
        p = self.doc.add_paragraph("")
        apply_paragraph_template(p, self.templates[kind])
        if text:
            run = p.add_run(clean_inline(text))
            copy_run_format(run, first_template_run(self.templates[kind]))
        return p

    def page_break(self):
        p = self.doc.add_paragraph("")
        p.add_run().add_break(WD_BREAK.PAGE)

    def bullet(self, text: str):
        return self.paragraph(f"- {clean_inline(text)}", "body")

    def numbered(self, text: str):
        return self.paragraph(clean_inline(text), "body")

    def figure(self, caption: str):
        self.doc.add_paragraph("")
        self.paragraph(caption, "figure")

    def table_caption(self, caption: str):
        self.paragraph(caption, "table_caption")

    def table(self, rows: list[list[str]], template_name: str = "two_col"):
        cols = max(len(row) for row in rows)
        table = self.doc.add_table(rows=len(rows), cols=cols)
        table.style = self.table_templates[template_name].style
        header_tpl = self.table_templates[template_name].rows[0].cells[0].paragraphs[0]
        body_tpl = self.table_templates[template_name].rows[min(1, len(self.table_templates[template_name].rows) - 1)].cells[0].paragraphs[0]
        for r_idx, row in enumerate(rows):
            for c_idx, value in enumerate(row):
                cell = table.cell(r_idx, c_idx)
                cell.text = clean_inline(value)
                for p in cell.paragraphs:
                    apply_paragraph_template(p, header_tpl if r_idx == 0 else body_tpl)
                    if p.runs:
                        copy_run_format(p.runs[0], first_template_run(header_tpl if r_idx == 0 else body_tpl))
        return table


def remove_tail(doc: Document, start_paragraph_index: int) -> None:
    body = doc._body._element
    start_element = doc.paragraphs[start_paragraph_index]._element
    started = False
    for child in list(body.iterchildren()):
        if child is start_element:
            started = True
        if started and child.tag != qn("w:sectPr"):
            body.remove(child)


def enable_update_fields(doc: Document) -> None:
    settings = doc.settings._element
    node = settings.find(qn("w:updateFields"))
    if node is None:
        node = OxmlElement("w:updateFields")
        settings.append(node)
    node.set(qn("w:val"), "true")


